/**
 * A composable that draws an animated border around the content.
 * https://gist.github.com/aabadaa/aee63b5348778be58c9c595469ff1957
 * https://medium.com/@mohammadbahadori99/creating-an-animated-border-composable-in-jetpack-compose-8604e4c7c66e
 */
@SuppressLint("ComposeComposableModifier")
@Composable
fun Modifier.animatedBorder(
    borderColors: ImmutableList<Color>,
    backgroundColor: Color,
    shape: Shape = RectangleShape,
    borderWidth: Dp = 1.dp,
    animationDurationInMillis: Int = 1000,
    easing: Easing = LinearEasing
) = composed {
    val brush = Brush.sweepGradient(borderColors)
    val infiniteTransition = rememberInfiniteTransition(label = "animatedBorder")
    val angle by infiniteTransition.animateFloat(
        initialValue = 0f,
        targetValue = 360f,
        animationSpec = infiniteRepeatable(
            animation = tween(durationMillis = animationDurationInMillis, easing = easing),
            repeatMode = RepeatMode.Restart
        ),
        label = "angleAnimation"
    )

    this.clip(shape)
        .padding(borderWidth)
        .drawWithContent {
            rotate(angle) {
                drawCircle(
                    brush = brush,
                    radius = size.width,
                    blendMode = BlendMode.SrcIn,
                )
            }
            drawContent()
        }
        .background(color = backgroundColor, shape = shape)
}